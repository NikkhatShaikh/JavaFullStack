package oprators;

public class AsignmentOprators {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x);

        int X = 110;
        X += 5;
        System.out.println(X); // same as X=X+5;

        int y = 10;
        y-=3;
        System.out.print(y);

        int z=15;
        z*=3;
        System.out.println(z);

        int a =5;
        a/=2;
        System.out.println(a);

        int b=20;
        b%=2;
        System.out.println(b);
    }
}