package oprators;

public class AllOprators {

    public static void main(String[] args) {
        int x = 40, y = 10;
        --x;  //  value decreases by 1
        --y;  // value  decreases by 1
        ++x; //  value increases by 1 so it converted into its orignal value
        ++y;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);


    }

}