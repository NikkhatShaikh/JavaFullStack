package controlStatement;

public class ElseIf {
    public static void main(String[] args) {
        int time = 10;
        if(time<=10){
            System.out.println("Good Morning");
        }if(time>12){
            System.out.println("Good Afternoon");
        }else{
            System.out.println("Have a good Day");
        }
    }
}
